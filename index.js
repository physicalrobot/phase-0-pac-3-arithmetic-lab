function add(x, y) {
    let a = x + y;
    return a;
}

function subtract(x, y) {
    let a = x - y;
    return a;
}

function multiply(x, y) {
    let a = x * y;
    return a;
}

function divide(x, y) {
    let a = x / y;
    return a;
}

function increment(n) {
    ++n;
    return n;
}

function decrement(n) {

    n--;
    return n;
}

function makeInt(n) {

    return parseInt(n, 10);

}
function preserveDecimal(n) {
    return parseFloat(n);
}